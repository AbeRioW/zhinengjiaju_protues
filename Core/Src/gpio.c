void led_control(bool open)
{
	HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, open?GPIO_PIN_RESET:GPIO_PIN_SET);
}

void fan_control(bool open)
{
	HAL_GPIO_WritePin(FAN_GPIO_Port, FAN_Pin, open?GPIO_PIN_SET:GPIO_PIN_RESET);
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
			if(GPIO_Pin==KEY1_Pin)
			{
				 HAL_GPIO_TogglePin(LED_GPIO_Port,LED_Pin);
				 key1_status=true;
			}
			
			if(GPIO_Pin==KEY2_Pin)
			{
					HAL_GPIO_TogglePin(FAN_GPIO_Port,FAN_Pin);
								 key2_status=true;
			}
}




